    private void getTrasport(Long idTransp, boolean isCharge) {
        this.isCharge = isCharge;
        if (isCharge) {
            transport = new ChargeRequest().select(new Charge(idTransp));
        } else {
            transport = new DischargeRequest().select(new Discharge(idTransp));
        }
    }

    private void initPanel() {
        TreatmentTheme.initTheme(pnlDetail);
        TreatmentTheme.iconDefine(btnPrint, "/static/img/pdf_white.png");
    }

    private void fillPanel() {
        lblProvider.setText(transport.getProvider().getNameProvider());
        lblDateEntry.setText(convertDateUtil(transport.getDateEntry()));
        lblTimeEntry.setText(String.valueOf(transport.getTimeEntry()));
        lblNote.setText(String.valueOf(transport.getNote()));
        lblTicket.setText(String.valueOf(transport.getTicket()));
        lblCarPlate.setText(transport.getCarPlate());
        lblCnh.setText(transport.getDriver().getCnhDriver());
        lblDriver.setText(transport.getDriver().getNameDriver());
        lblDateExit.setText(convertDateUtil(transport.getDateExit()));
        lblTimeExit.setText(String.valueOf(transport.getTimeExit()));
        lblProduct.setText(transport.getProduct().getNameProduct());
        lblAcidity.setText(String.valueOf(transport.getAnalyzeTruck().getAcidityTruck()));
        lblSaponity.setText(String.valueOf(transport.getAnalyzeTruck().getSaponityTruck()));
        lblDensity.setText(String.valueOf(transport.getAnalyzeTruck().getDensityTruck()));
        lblTrash.setText(String.valueOf(transport.getAnalyzeTruck().getTrashTruck()));
        lblBurden.setText(String.valueOf(transport.getBurden()));
        lblBurden.setText(String.valueOf(transport.getLiter()));
        lblTank.setText(transport.getTank().getNameTank());
    }
