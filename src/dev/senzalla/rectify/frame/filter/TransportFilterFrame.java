    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        if (isCharge) {
            ChargeTreatment.setTableFilters(tableTransport, Long.valueOf(spnIdTransport.getValue().toString()), dtcDateOf.getDate(), dtcDateUntil.getDate(), (Integer) spnTicket.getValue(), (Integer) spnNote.getValue(), cbxProduct.getSelectedItem(), cbxProvider.getSelectedItem());
        } else {
            DischargeTreatment.setTableFilters(tableTransport, Long.valueOf(spnIdTransport.getValue().toString()), dtcDateOf.getDate(), dtcDateUntil.getDate(), (Integer) spnTicket.getValue(), (Integer) spnNote.getValue(), cbxProduct.getSelectedItem(), cbxProvider.getSelectedItem());
        }
        this.dispose();
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnClearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClearActionPerformed
        if (isCharge) {
            ChargeTreatment.initTable(tableTransport);
        } else {
            DischargeTreatment.initTable(tableTransport);
        }
        this.dispose();
    }//GEN-LAST:event_btnClearActionPerformed

