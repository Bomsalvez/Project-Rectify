    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        if (isCharge) {
            new ChargeTreatment().initTableWithClause(tbl, spnCod, dtcDtOf, dtcDtUp, spnTicket, spnNote, cbxProduct, cbxProvider);
        } else {
            new TreatmentDcharge().showTable(tbl, spnCod, dtcDtOf, dtcDtUp, spnTicket, spnNote, cbxProduct, cbxProvider);
        }
        this.dispose();
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnClearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClearActionPerformed
        if (isCharge) {
            new ChargeTreatment().initTable(tbl, null,null);
        } else {
            new TreatmentDcharge().showTable(tbl);
        }
        this.dispose();
    }//GEN-LAST:event_btnClearActionPerformed

