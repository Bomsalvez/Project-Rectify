    private void showTable() {
        DefaultTableModel model = (DefaultTableModel) tblTank.getModel();
        model.setNumRows(0);

        new TankRequest().select().forEach(tank
                -> model.addRow(new Object[]{
            tank.getNameTank(),
            tank.getCapacityTank()
        }));
    }
