    public void minusLitters() {
        if (!txtMatterLiter.getText().equals("")) {
            new TreatmentPnlMatter().minusLitters(count);
            txtTransAmount.setText(String.valueOf(TreatmentPnlMatter.getLitters()));
        }
    }

